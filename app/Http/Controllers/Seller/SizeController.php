     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $siz_name=$request['size'];
        $size_name=SizeMaster::where('size',$siz_name)->value('size');

        if($size_name==null) {
            $size = new SizeMaster();
            $size->size=$request['size'];
            $size->save();

            flash('Size Inserted....');
            return back();

        }
        else
        {
            flash('Size Already Exist....');
            return back();

        }
    }

