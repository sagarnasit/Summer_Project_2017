    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $clr_name=$request['colorname'];
      $color_name=ColorMaster::where('color_name',$clr_name)->value('color_name');

      if($color_name==null) {
          $color = new ColorMaster();
          $color->color_name=$request['colorname'];
          $color->save();

          flash('Color Inserted....');
          return back();

      }
      else
      {
          flash('Color Already Exist....');
          return back();

      }
    }

